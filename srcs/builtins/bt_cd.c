#include "../../hdrs/minishell.h"

int	ft_cd(t_data *data, t_cmd *cmd)
{
	if (!cmd || !cmd->args || !cmd->args[0] || !cmd->args[1])
	{
		return (ft_cd_no_arg(data));
	}
	else
	{
		return (ft_cd_with_arg(data, cmd));
	}
}

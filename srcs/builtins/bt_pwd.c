#include "../../hdrs/minishell.h"

int	ft_pwd(t_data *data)
{
	char	*cwd;

	cwd = ft_getenv(data, "PWD");
	if (!cwd)
	{
		data->exit_status = 1;
		return (ERROR);
	}
	printf("%s\n", cwd);
	free(cwd);
	data->exit_status = 0;
	return (0);
}

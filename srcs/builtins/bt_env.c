#include "../../hdrs/minishell.h"

int	ft_env(t_data *data, char **env)
{
	int	i;

	if (!env || !(*env))
	{
		data->exit_status = 1;
		return (0);
	}
	i = 0;
	while (env[i])
	{
		if (ft_strchr(env[i], '='))
			printf("%s\n", env[i]);
		i++;
	}
	data->exit_status = 0;
	return (0);
}
